             <h2 class="topic"><b>Donor selection criteria</b></h2>
            <img src="images/Donate_page2.jpg" height="100%" width="100%">
            
            <p>In a blood donation system, ensuring the safety of both donors and recipients is paramount. Donor selection criteria help identify individuals who are suitable to donate blood while minimizing the risk of transfusion-transmissible infections and adverse reactions. Here are common donor selection criteria:</p>
            <ul>
            
            <li>Age:</li>
            Donors typically need to be within a certain age range, often between 18 to 65 years old. The minimum age may vary by jurisdiction and organization.
            <li>Weight:</li>
            Donors must weigh above a certain threshold, usually around 50 kilograms (110 pounds), to ensure they can safely donate without experiencing adverse effects.
            <li>Health Status:</li>
            Donors should be in good health and free from acute or chronic illnesses at the time of donation.<br>
            Individuals with conditions such as cold, flu, or infections should postpone donation until they have fully recovered.
            <li>Travel History:</li>
            Donors may be deferred based on recent travel to regions with endemic diseases or outbreaks, such as malaria or Zika virus-affected areas.
            <li>Medical History:</li>
            Donors are asked to provide information about their medical history, including past surgeries, blood transfusions, and chronic conditions.
            
    </div>

