        <img src="images/Donate_page3.jpg" width="100%"; height="100%">
        <ul>
        <li>Donors who engage in behaviors associated with an increased risk of transfusion-transmissible infections, such as intravenous drug use or unprotected sexual activity with multiple partners, may be deferred.</li>
        <li>Individuals who have received tattoos or piercings within a specified time frame (usually within the past 6 to 12 months) may be deferred due to the risk of bloodborne infections.</li>
        <li>Donors who have lived in or traveled to regions with a high prevalence of specific infectious diseases, such as variant Creutzfeldt-Jakob disease (vCJD) or malaria, may be deferred for a specified period.</li>
        <li>Military personnel and individuals deployed to certain regions may have additional criteria based on exposure to infectious diseases or environmental factors.</li>
        <li>Pregnant individuals are usually deferred from blood donation due to the physiological changes and potential risks to both the donor and the recipient.</li>
        <li>Individuals with a history of hepatitis B or C, HIV/AIDS, or other bloodborne infections are typically deferred from blood donation.</ul></li></p>
